import base.SpecBase
import play.api.libs.json.{JsObject, Reads}

trait JsonModelBaseSpec extends SpecBase {
  def testJsonToModel[A](scenario: String,
                         json: JsObject,
                         model: A)(implicit reads: Reads[A]): Unit = {

    s"${model.getClass.getSimpleName}" when {

      s"$scenario" must {

        "convert JSON to a model correctly" in {
          json.as[A] mustBe model
        }
      }
    }
  }
}
